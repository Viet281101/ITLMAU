Map::Map() {
	//// Load image
	ceramic_tile_1 = TextureManager::LoadTexture("res/world/ceramic_tiles/1.png");
	ceramic_tile_2 = TextureManager::LoadTexture("res/world/ceramic_tiles/2.png");
	ceramic_tile_3 = TextureManager::LoadTexture("res/world/ceramic_tiles/3.png");
	ceramic_tile_4 = TextureManager::LoadTexture("res/world/ceramic_tiles/4.png");
	ceramic_tile_5 = TextureManager::LoadTexture("res/world/ceramic_tiles/5.png");
	ceramic_tile_6 = TextureManager::LoadTexture("res/world/ceramic_tiles/6.png");

	sewer_1 = TextureManager::LoadTexture("res/world/hole_ground/5.png");
	sewer_2 = TextureManager::LoadTexture("res/world/hole_ground/6.png");

	loadMap(level_1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}


Map::~Map()
{
	SDL_DestroyTexture(ceramic_tile_1);
	SDL_DestroyTexture(ceramic_tile_2);
	SDL_DestroyTexture(ceramic_tile_3);
	SDL_DestroyTexture(ceramic_tile_4);
	SDL_DestroyTexture(ceramic_tile_5);
	SDL_DestroyTexture(ceramic_tile_6);
	SDL_DestroyTexture(sewer_1);
	SDL_DestroyTexture(sewer_2);
}


void Map::loadMap(int arr[30][30]) {
	for ( int row = 0; row < 30; ++row ) {
		for ( int col = 0; col < 30; col++ ) {
			map[row][col] = arr[row][col];
		}
	}
}

void Map::drawMap() {
	int type = 0;
	for ( int row = 0; row < 30; ++row ) {
		for ( int col = 0; col < 30; col++ ) {
			type = map[row][col];

			dest.x = col * 32;
			dest.y = row * 32;

			switch ( type ) {
				case 0: 
					TextureManager::Draw( ceramic_tile_1, src, dest);
					break;
				case 1:
					TextureManager::Draw( ceramic_tile_2, src, dest);
					break;
				case 2:
					TextureManager::Draw( ceramic_tile_3, src, dest);
					break;
				case 3:
					TextureManager::Draw( ceramic_tile_4, src, dest);
					break;
				case 4:
					TextureManager::Draw( ceramic_tile_5, src, dest);
					break;
				case 5:
					TextureManager::Draw( ceramic_tile_6, src, dest);
					break;
				case 6:
					TextureManager::Draw( sewer_1, src, dest);
					break;
				case 7:
					TextureManager::Draw( sewer_2, src, dest);
					break;

				default:
					break;
			}
		}
	}
}


